public class Person{
    
String name;
String DateOfBirth;

    public void setName(String name) {
        this.name = name;
    }

    public void setDateOfBirth(String DateOfBirth) {
        this.DateOfBirth = DateOfBirth;
    }

   
    public String getName() {
        return name;
    }

    public String getDateOfBirth() {
        return DateOfBirth;
    }
    

}